 #ifndef _INC_SLIST
 #define _INC_SLIST
 #pragma comment(lib,"..\\lib\\slist.lib")    
 #include <stdio.h>

 class Item
 {
    friend class SList;
    public:
     int  numb;  /* номер на записа */
     int value;//стойност
     Item *next; /*наследник */
     Item *back;/* предшественик */  
     Item(int ,int ,Item *);  //конструктор
     };

/* Декларация на клас SList */    
 class SList
 {     
   public:
   Item *top,**cur,*bcur;  /* указатели към началният,текущият и последният елементи */
   int nb;  /* брой възли */
   SList(); /* конструктор */
   ~SList();/* деструктор */
   void find(int );  /* търсене по ключ-номер */
   void removeall(Item **);/* изтриване на списъка */
   void add(int ,int ); /* добавяне на възел */                                           
   void saveall(char *);/* запис във файл на списъка */
  };

#endif
 
